#!/usr/bin/env python3
"""
Script para remover a coluna cod_categoria da tabela cadprod
"""
import sys
import os

# Adicionar o diret√≥rio do projeto ao path
sys.path.append(os.path.dirname(__file__))

try:
    from sqlalchemy import text
    from app.database.db_connection import engine
    
    print("üîÑ Removendo coluna cod_categoria da tabela cadprod...")
    
    with engine.connect() as conn:
        # Verificar se a coluna existe
        result = conn.execute(text("""
            SELECT EXISTS (
                SELECT 1 FROM information_schema.columns 
                WHERE table_schema = 'mensura' 
                AND table_name = 'cadprod'
                AND column_name = 'cod_categoria'
            );
        """))
        
        if result.scalar():
            print("‚úÖ Coluna cod_categoria encontrada")
            
            # Remover foreign key primeiro
            print("üîÑ Removendo foreign key...")
            conn.execute(text("""
                ALTER TABLE mensura.cadprod 
                DROP CONSTRAINT IF EXISTS cadprod_cod_categoria_fkey;
            """))
            
            # Remover a coluna
            print("üîÑ Removendo coluna cod_categoria...")
            conn.execute(text("""
                ALTER TABLE mensura.cadprod 
                DROP COLUMN IF EXISTS cod_categoria;
            """))
            
            conn.commit()
            print("‚úÖ Coluna cod_categoria removida com sucesso!")
        else:
            print("‚ÑπÔ∏è Coluna cod_categoria n√£o existe")
        
        # Verificar se foi removida
        result = conn.execute(text("""
            SELECT EXISTS (
                SELECT 1 FROM information_schema.columns 
                WHERE table_schema = 'mensura' 
                AND table_name = 'cadprod'
                AND column_name = 'cod_categoria'
            );
        """))
        
        if not result.scalar():
            print("‚úÖ Confirma√ß√£o: Coluna cod_categoria foi removida")
        else:
            print("‚ùå Erro: Coluna ainda existe")
    
except Exception as e:
    print(f"‚ùå Erro: {e}")
    import traceback
    traceback.print_exc()
    sys.exit(1)
